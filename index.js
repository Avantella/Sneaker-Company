function openHamburgerMenu() {  
    let x = document.getElementById("mobile-nav");
    if (x.style.display === "none") {
      x.style.display = "flex";
    } else {
      x.style.display = "none";
    }
  } 

  function closeHamburgerMenu() {
   
    let x = document.getElementById("mobile-nav");
    if (x.style.display === "none") {
      x.style.display = "flex";
    } else {
      x.style.display = "none";
    }
  }
